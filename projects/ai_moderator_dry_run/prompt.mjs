function truncateText(value, maxLength) {
  const text = String(value || '').trim();
  if (!text) {
    return '';
  }
  const limit = Math.max(1, Number(maxLength) || text.length);
  return text.length > limit ? `${text.slice(0, limit)}...` : text;
}

